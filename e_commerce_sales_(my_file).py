"""## 8. Handling Missing Values — `isnull`, `fillna`, `dropna`"""

data.isnull().sum()

data

# Fill numeric missing values with the column median
data['Age'] = data['Age'].fillna(data['Age'].median())
print(data['Age'])
data['Rating'] = data['Rating'].fillna(data['Rating'].median())
print(data['Rating'])
data['Discount'] = data['Discount'].fillna(data['Discount'].median())
print(data['Discount'])
data['Total_Amount'] = data['Total_Amount'].fillna(data['Total_Amount'].median())
print(data['Total_Amount'])


# Fill categorical missing values with a placeholder
data['City'] = data['City'].fillna('Unknown')
print(data['City'])
data['Customer_Satisfaction'] = data['Customer_Satisfaction'].fillna('Not Provided')
print(data['Customer_Satisfaction'])

data[['Age', 'Rating', 'Discount','Total_Amount', 'City', 'Customer_Satisfaction']].isnull().sum()

# dropna() example: drop rows still missing critical values (e.g. Delivery_Date, Profit)
# (shown here without overwriting df, just to see the effect)
cleaned_subset = data.dropna(subset=['Delivery_Date', 'Profit'])
print("Rows before dropna:", data.shape[0])
print("Rows after dropna on Delivery_Date & Profit:", cleaned_subset.shape[0])

"""## 9. Duplicate Handling"""

print(data.duplicated().sum())   #rows

# Dup_orderID = data[data.duplicated(subset='Order_ID', keep=False)]    # keep=False -> jitni bhi dup_order ids hai sabko True mark karta hai
# print(Dup_orderID.shape[0])   # no. of dup_orderid = 231
