data = data.drop_duplicates(subset='Order_ID', keep='first')
print(data['Order_ID'].duplicated().sum())
data.shape

"""## 10. `groupby`, `agg`, and `transform`"""

# Average Total_Amount per Product_Category
avg_amount = data.groupby('Product_Category')['Total_Amount'].mean()
avg_amount.sort_values(ascending=False)

# agg(): multiple aggregations at once, grouped by Region
data.groupby('Region').agg(
    Total_Revenue=('Revenue', 'sum'),
    Avg_Order_Value=('Total_Amount', 'mean'),
    Orders=('Order_ID', 'count')
).sort_values('Total_Revenue', ascending=False)

data['Category_Avg_Amount'] = data.groupby('Product_Category')['Total_Amount'].transform('mean')
data[['Product_Category', 'Total_Amount', 'Category_Avg_Amount']]

"""## 11. Data Type Fixing (`astype`)"""

data.dtypes

# Clean 'Gender' -> strip spaces, standardize case
data['Gender'] = data['Gender'].str.strip().str.upper()
data['Gender'] = data['Gender'].replace({'MALE': 'Male', 'FEMALE': 'Female', 'M': 'Male', 'F': 'Female'})
print(data['Gender'].unique())

# Clean 'Unit_Price' -> remove commas, then convert to float
data['Unit_Price'] = data['Unit_Price'].astype(str).str.replace(',', '', regex=False).astype(float)

# Clean 'Profit' -> remove currency symbol (₹), then convert to float
data['Profit'] = data['Profit'].astype(str).str.replace('₹', '', regex=False).str.replace(',', '', regex=False)
data['Profit'] = pd.to_numeric(data['Profit'], errors='coerce')

# Fix Age -> whole numbers make more sense than floats
data['Age'] = data['Age'].astype(int)

data[['Unit_Price', 'Profit', 'Age']].dtypes

data['Order_Date'] = pd.to_datetime(data['Order_Date'], errors='coerce', format='mixed', dayfirst=False)
data['Delivery_Date'] = pd.to_datetime(data['Delivery_Date'], errors='coerce', format='mixed', dayfirst=False)

data[['Order_Date', 'Delivery_Date']].dtypes

data.info()