hrs+mins

def clean_duration(duration):
  if pd.isnull(duration):
    return np.nan

  if 'h' in duration:
    hours, minutes = duration.split('h')
    hours = int(hours) * 60
    minutes = int(minutes.replace('m',''))
    return hours + minutes

  elif "min" or "mins" in duration:
    duration = duration.replace('mins','').replace('min','')
    return int(duration)

  else:
    return int(duration)

